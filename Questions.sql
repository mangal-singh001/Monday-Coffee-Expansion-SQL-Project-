-- Objective


-- The goal of this project is to analyze the sales data of Monday Coffee, a company that has been selling its products online since 
-- January 2023, and to recommend the top three major cities in India for opening new coffee shop locations based on consumer demand and 
-- sales performance.



-- Key Questions


-- Coffee Consumers Count
-- How many people in each city are estimated to consume coffee, given that 25% of the population does?


-- Total Revenue from Coffee Sales
-- What is the total revenue generated from coffee sales across all cities in the last quarter of 2023?


-- Sales Count for Each Product
-- How many units of each coffee product have been sold?


-- Average Sales Amount per City
-- What is the average sales amount per customer in each city?


-- City Population and Coffee Consumers
-- Provide a list of cities along with their populations and estimated coffee consumers.


-- Top Selling Products by City
-- What are the top 3 selling products in each city based on sales volume?


-- Customer Segmentation by City
-- How many unique customers are there in each city who have purchased coffee products?


-- Average Sale vs Rent
-- Find each city and their average sale per customer and avg rent per customer


-- Monthly Sales Growth
-- Sales growth rate: Calculate the percentage growth (or decline) in sales over different time periods (monthly).


-- Market Potential Analysis
-- Identify top 3 city based on highest sales, return city name, total sale, total rent, total customers, estimated coffee consumer

